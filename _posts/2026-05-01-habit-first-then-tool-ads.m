ADS（AI-Driven School）の4月課題のテーマは、**「面倒な報告を自動化するツールを作る」**。

最終的に再設計に踏み切ることになりました。その経緯を残しておきます。

---

## 朝の「迷子問題」から始まった

毎朝、「今日何をすればいいか」が分かりませんでした。

ツールは分散していて、判断材料を集めるだけで時間が過ぎてしまいます。1日が終わっても、何ができたか曖昧なまま。

欲しかったのはシンプルで、**「朝イチでぱっと見て、今日の動き方が分かるもの」** でした。

ただ、これは課題を作る段になって初めて気づいたことです。「自分にとっての面倒な報告って何だろう？」と問い直したとき、**毎朝の状況把握そのものが面倒だった**ということが、ようやくはっきりしました。課題を進める前には、ここまで言語化できていませんでした。

---

## 最初のプロトタイプ

今月の課題をスタートするとき、まず「自分にとっての面倒な報告＝朝の状況報告」という一点だけを手がかりに、作り始めました。

Cursorを使ってHTMLで試作したのが最初のプロトタイプです。

- iPhone（入力）→ PC（出力）の二段階設計
- 体調・固定スケジュール・やりたいことを入力すると、今日のダッシュボードが表示される

入力と出力を別デバイスで完結させる設計は悪くありませんでした。  
ただ、一つ引っかかっていました。

> **「表示される項目が、本当に自分に必要なものなのか」**

それが分からないまま、次のフェーズへ進むことになりました。

---

## Claudeとの対話で「面倒」を分解した（Phase 2）

プロトタイプを作った後、スクールの2回目の講義を受けました。

そこで大きな気づきがありました。**「面倒をどれだけ細かく分解できているか」** が、ツール設計の精度を決める、という話です。

そこで一度立ち止まり、**このモーニングダッシュボードで「そもそも何が面倒なのか」を分解する作業を、Claudeに手伝ってもらう**ことから始めました。

Claudeとの対話の中で見えてきたこと：

- 多くのタスク管理ツールは、自分の生活スタイルに合っていない
- パートナーと24時間ほぼ一緒に生活しているため、**パートナーのスケジュールや希望が「外部アンカー」として機能している**
- フリーランスゆえに固定の枠がない──だからこそ、**外部アンカーから逆算する設計**が必要

> **外部アンカー：** 自分以外の要因（人・予定）が、その日の行動の「基準点」になること。フリーランスには、自分ではコントロールできない外部の固定点を逆算の起点にする設計が有効です。
{: .prompt-info }

---

## 一度、課題を諦めた

面倒を分解していくうちに、Claudeとの対話はどんどん深くなっていきました。

そして行き着いたのが、**「面倒の根っこは、自分のライフスタイルそのものにある」** という結論でした。

正直、これはショックでした。ツールを作ろうとしていたのに、問題はもっと手前にある、という話だったからです。

課題をそのまま進める気にはなれませんでした。「このまま続けても、本当に使えるものにはならない」という感覚が強くありました。

なので一旦、課題を進めることを諦めました。

その代わり、**「朝に何をしたいのか」「自分が本当に欲しいのは何か」をClaudeとの対話の中で先に明らかにすること**から始めました。

- 体調と予定を伝える → Claudeが今日やること3つを提案
- 残ったものは保留タスクとしてストック
- ログをNotionに記録

これを習慣として続けるうちに、**「何を入れて、何を見たいか」が、体感としてつかめてきました。**

頭で考えるだけでは出てこなかった答えが、使いながら見えてきたのです。

> 設計図より先に「使う体験」があると、必要な情報の解像度が上がります。ツール設計でこれを意図的にやるのは、なかなか難しいことです。
{: .prompt-tip }

---

## なぜ今、再設計するのか

理由はいくつか重なりました。

まず、**課題の提出期限が来週に迫っていること**。このまま最初のプロトタイプだけで今月を終えるのは嫌でした。少しでも動くものを作って出したい、という気持ちがありました。

もう一つは、**発想の転換**がきっかけです。

AIニュースを自分なりにカスタマイズしながら、CursorやClaudeとの対話を続けていくうちに、あることに気がつきました。**朝のルーチンを作るためにClaudeのスキルとして育ててきたもの自体が、そのままかなりのアプリになるのではないか**、という発想です。

習慣として積み上げてきた対話の蓄積が、そのままツールの設計図になっている。それなら、もう一度ちゃんと再設計する意味があると判断しました。

再設計で入れたいものは、すでに明確になっています：

1. **Claudeとの対話で育てた習慣を、そのまま反映した設計**（ぱっと見の情報に穴がない状態）
2. **ダッシュボードからClaudeチャットを呼び出せるボタン**
3. 将来的には、**ダッシュボード内にチャット画面を埋め込む**設計へ

---

## 一言でまとめると

> 「作る前に使い、使いながら設計した。ツールの見た目より先に、自分の習慣と必要な情報が明確になったから、今度こそ本当に使えるものが作れます。」

---

💡 **OHOポイント：**  
「まず作って試す」はよく言われます。でも今回やったのはその逆──**まず使う習慣を作り、そこから設計を逆算しました**。ツールの完成度より、自分の行動が変わるかどうかが先、という順序の話です。
